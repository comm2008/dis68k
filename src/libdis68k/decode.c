#define bit_n(input, i)    ((input & (0x1 << i)) >> i)
#define nybble_n(input, i) ((input & (0xF << (i*4))) >> (i*4))

int decode_instr(uint16_t *encoded, i68k *decoded)
{
	// Divide the encoded instruction into nybbles.
	char part_0 = nybble_n(encoded_instr, 0);
	char part_1 = nybble_n(encoded_instr, 1);
	char part_2 = nybble_n(encoded_instr, 2);
	char part_3 = nybble_n(encoded_instr, 3);

	char bit_0 = bit_n(encoded_instr, 0x0);
	char bit_1 = bit_n(encoded_instr, 0x1);
	char bit_2 = bit_n(encoded_instr, 0x2);
	char bit_1 = bit_n(encoded_instr, 0x1);
	char bit_3 = bit_n(encoded_instr, 0x3);
	char bit_4 = bit_n(encoded_instr, 0x4);
	char bit_5 = bit_n(encoded_instr, 0x5);
	char bit_6 = bit_n(encoded_instr, 0x6);
	char bit_7 = bit_n(encoded_instr, 0x7);
	char bit_8 = bit_n(encoded_instr, 0x8);
	char bit_9 = bit_n(encoded_instr, 0x9);
	char bit_A = bit_n(encoded_instr, 0xA);
	char bit_B = bit_n(encoded_instr, 0xB);
	char bit_C = bit_n(encoded_instr, 0xC);
	char bit_D = bit_n(encoded_instr, 0xD);
	char bit_E = bit_n(encoded_instr, 0xE);
	char bit_F = bit_n(encoded_instr, 0xF);

	// Decode whole-word-constant instructions
	switch(encoded_instr)
	{
		case 0x4E71:
			// NOP
			break;
		case 0x4E70:
			// RESET
			break;
		case 0x4E73:
			// RTE
			break;
		case 0x4E75:
			// RTS
			break;
		case 0x4E77:
			// RTR
			break;
		case 0x4E72:
			// STOP ++ 16-bit immediate
			break;
		case 0x4E76:
			// TRAPV
			break;
		case 0x003C:
			// ORI_to_CCR
			break;
		case 0x007C:
			// ORI_to_SR
			break;
		case 0x023C:
			// ANDI_to_CCR
			break;
		case 0x027C:
			// ANDI_to_SR
			break;
		case 0x0A3C:
			// EORI_to_CCR
			break;
		case 0x0A7C:
			// EORI_to_SR
			break;
	}

	switch(part_3)
	{
		case 0x0:
			switch(part_2)
			{
				case 0x0:
					// ORI
					break;
				case 0x2:
					// ANDI
					break;
				case 0x4:
					// SUBI
					break;
				case 0x6:
					// ADDI
					break;
				case 0xA:
					// EORI
					break;
				case 0xC:
					// CMPI
					break;
				default:
					switch((encoded_instr & 0x01C0) >> 6)
					{
						case 0x4:
							// BTST
							break;
						case 0x5:
							// BCHG
							break;
						case 0x6:
							// BCLR
							break;
						case 0x7:
							// BSET
							break;
						default:
							if(bit_8 && !bit_5 && !bit_4 && bit_3)
							{
								// MOVEP
							}
							else
							{
								// Error
							}
					}
					break;
			}
			break;
		case 0x4:
			if(bit_8 && !bit_6)
			{
				// CHK
			}
			else if(bit_8 && bit_7 && bit_6)
			{
				// LEA
			}
			else if(bit_B && !bit_9 && !bit_8 && bit_7)
			{
				// MOVEM
			}
			else
			{
				switch(part_2)
				{
					case 0x0:
						// MOVE_from_SR, part_2 == 0x0 && bit_7 && bit_6
						// NEGX, part_2 == 0x0
						break;
					case 0x2:
						// MOVE_from_CCR, part_2 == 0x2 && bit_7 && bit_6
						// CLR
						break;
					case 0x4:
						// MOVE_to_CCR, part_2 == 0x4 && bit_7 && bit_6
						// NEG, part_2 == 0x4
						break;
					case 0x6:
						// MOVE_to_SR, part_2 == 0x6 && bit_7 && bit_6
						break;
					case 0x8:
						// EXT, part_2 == 0x8
						// NBCD, part_2 == 0x8 && !bit_7 && !bit_6
						// PEA, part_2 == 0x8 && !bit_7 && bit_6
						// SWAP, part_2 == 0x8 && part_1 == 0x4 && bit_3
						break;
					case 0xA:
						// ILLEGAL, part_2 == 0xA
						// TAS, part_2 == 0xA && bit_7 && bit_6
						// TST, part_2 == 0xA
						break;
					case 0xE:
						// JMP, part_2 == 0xE && bit_7 && bit_6
						// JSR, part_2 == 0xE && bit_7 && !bit_6
						// LINK, part_2 == 0xE && part_1 == 0x5
						// MOVE_USP, part_2 == 0xE && part_1 == 0x6
						// UNLK, part_2 == 0xE && part_1 == 0x5 && bit_3
						break;
				}
			}
			break;
		case 0x5:
			if(part_1 == 0xB && bit_3)
			{
				// DBcc
			}
			else if(bit_7 && bit_6)
			{
				// Scc
			}
			else if(bit_8)
			{
				// SUBQ
			}
			else
			{
				// ADDQ
			}
			break;
		case 0x6:
			switch(part_2)
			{
				case 0x0:
					// BRA
					break;
				case 0x1:
					// BSR
					break;
				default:
					// Bcc
					break;
			}
			break;
		case 0x7:
			// MOVEQ
			break;
		case 0x8:
			if(part_1 == 0x0 && bit_8)
			{
				// SBCD
			}
			else if(!bit_8 && bit_7 && bit_6)
			{
				// DIVU
			}
			else if(bit_8 && bit_7 && bit_6)
			{
				// DIVS
			}
			else
			{
				// OR
			}
			break;
		case 0x9:
			if(bit_7 && bit_6)
			{
				// SUBA
			}
			else if(bit_8 && !bit_5 && !bit_4)
			{
				// SUBX
			}
			else
			{
				// SUB
			}
			break;
		case 0xB:
			if(bit_7 && bit_6)
			{
				// CMPA
			}
			else if(bit_8 && !bit_5 && !bit_4 && bit_3)
			{
				// CMPM
			}
			else if(bit_8)
			{
				// EOR
			}
			else
			{
				// CMP
			}
			break;
		case 0xC:
			if(part_1 == 0x0 && bit_8)
			{
				// ABCD
			}
			else if(bit_8 && !bit_5 && !bit_4)
			{
				// EXG
			}
			else if(part_2 == 0x6)
			{
				// NOT
			}
			else
			{
				// AND
			}
			break;
		case 0xD:
			if(bit_7 && bit_6)
			{
				// ADDA
			}
			else if(bit_8 && !bit_5 && !bit_4)
			{
				// ADDX
			}
			else
			{
				// ADD
			}
			break;
		case 0xE:
			if(bit_7 && bit_6)
			{
				// Memory Shift Instructions //
				switch(part_2)
				{
					case 0x0:
						// ASR
						break;
					case 0x1:
						// ASL
						break;
					case 0x2:
						// LSR
						break;
					case 0x3:
						// LSL
						break;
					case 0x4:
						// ROXR
						break;
					case 0x5:
						// ROXL
						break;
					case 0x6:
						// ROR
						break;
					case 0x7:
						// ROL
						break;
				}
			}
			else if(!bit_4 && !bit_3)
			{
				// Register Shift Instructions //
				switch(((bit_4 << 2)  | (bit_3 << 1) | bit_8) & 0x7)
				{
					case 0x0:
						// ASR
						break;
					case 0x1:
						// ASL
						break;
					case 0x2:
						// LSR
						break;
					case 0x3:
						// LSL
						break;
					case 0x4:
						// ROXR
						break;
					case 0x5:
						// ROXL
						break;
					case 0x6:
						// ROR
						break;
					case 0x7:
						// ROL
						break;
				}
			}
			break;
		case 0x1:
		case 0x2:
		case 0x3:
		case 0xA:
		case 0xF:
			// <NONE>
			break;
	}
}

